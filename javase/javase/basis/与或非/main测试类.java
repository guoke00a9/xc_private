package javase.basis.与或非;

public class main测试类 {

	public static void main(String[] args) {

		/**
		 * ^运算是对二进制的补码进行运算
		 * 负数的最高位为符号位1
		 * 负数的补码为：
		 * 		取反+1
		 * -12		源码 1000 1100	反码 1111 0011		补码 1111 0100
		 * 11		源码 0000 1011	反码 0000 1011		补码 0000 1011
		 * ^					异或运算结果为  			1111 1111
		 * 						然后进行取反操作 符号位不变	1000 0000
		 * 						最后+1【高位1代表负数】		1000 0001 == -1
		 */
		System.out.println(11^-12);
	}

}
